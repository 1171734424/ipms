package com.ideassoft.bean.wrapper;

import java.io.Serializable;

public class CheckoutRoom implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4058720056501653109L;

	private String checkId;
	private String projectid;
	private String project;
	private String amount;
	private String subprice;
	private String remark;

	@Override
	public String toString() {
		return "CheckoutRoom [amount=" + amount + ", checkId=" + checkId
				+ ", project=" + project + ", projectid=" + projectid
				+ ", remark=" + remark + ", subprice=" + subprice + "]";
	}

	public String getCheckId() {
		return checkId;
	}

	public void setCheckId(String checkId) {
		this.checkId = checkId;
	}

	public String getProjectid() {
		return projectid;
	}

	public void setProjectid(String projectid) {
		this.projectid = projectid;
	}

	public String getProject() {
		return project;
	}

	public void setProject(String project) {
		this.project = project;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getSubprice() {
		return subprice;
	}

	public void setSubprice(String subprice) {
		this.subprice = subprice;
	}

}
