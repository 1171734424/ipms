package com.ideassoft.bean;

public class CleanInfo {

	private String cleanTime;
	private String timeArea;
	private String restAmount;
	private String roomId;
	private String cleanPerson;
	private String weekday;

	public String getWeekday() {
		return weekday;
	}

	public void setWeekday(String weekday) {
		this.weekday = weekday;
	}

	public String getCleanTime() {
		return cleanTime;
	}

	public void setCleanTime(String cleanTime) {
		this.cleanTime = cleanTime;
	}

	public String getTimeArea() {
		return timeArea;
	}

	public void setTimeArea(String timeArea) {
		this.timeArea = timeArea;
	}

	public String getRestAmount() {
		return restAmount;
	}

	public void setRestAmount(String restAmount) {
		this.restAmount = restAmount;
	}

	public String getRoomId() {
		return roomId;
	}

	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}

	public String getCleanPerson() {
		return cleanPerson;
	}

	public void setCleanPerson(String cleanPerson) {
		this.cleanPerson = cleanPerson;
	}

}
